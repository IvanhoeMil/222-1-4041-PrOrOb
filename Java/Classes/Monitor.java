package Java.Classes;

public class Monitor {
    public int id;
    public String brand;
    public String size;
    public String resolution;
    public int refresh_rate;
    public String video_input;

    public Monitor(int id, String brand, String size, String resolution, int refresh_rate, String video_input) {
        this.id = id;
        this.brand = brand;
        this.size = size;
        this.resolution = resolution;
        this.refresh_rate = refresh_rate;
        this.video_input = video_input;
    }
}
