package Java.Classes;
public class Keyboard {
    public int id;
    public String brand;
    public String model;
    public String color;
    public String size;

    public Keyboard(int id, String brand, String model, String color, String size) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.color = color;
        this.size = size;
    }
}
