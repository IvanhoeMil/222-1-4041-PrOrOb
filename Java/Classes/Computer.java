package Java.Classes;
public class Computer {
    public int id;
    public String computer_case;
    public String motherboard;
    public String processor;
    public String memory;
    public String gpu;
    public String data_storage_device;
    public String monitor;
    public String keyboard;

    public Computer(int id, String computer_case, String motherboard, String processor, String memory, String gpu, String data_storage_device, String monitor, String keyboard) {
        this.id = id;
        this.computer_case = computer_case;
        this.motherboard = motherboard;
        this.processor = processor;
        this.memory = memory;
        this.gpu = gpu;
        this.data_storage_device = data_storage_device;
        this.monitor = monitor;
        this.keyboard = keyboard;
    }
    



}
