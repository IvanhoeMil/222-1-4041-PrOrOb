public class Memory {
    public int id;
    public String brand;
    public String model;
    public String memory_type;
    public int memory_size;

    public Memory(int id, String brand, String model, String memory_type, int memory_size) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.memory_type = memory_type;
        this.memory_size = memory_size;
    }
}
