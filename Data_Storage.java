public class Data_Storage {
    public int id;
    public String brand;
    public String model;
    public int memory_size;
    public String memory_type;

    public Data_Storage(int id, String brand, String model, int memory_size, String memory_type) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.memory_size = memory_size;
        this.memory_type = memory_type;
    }
}
